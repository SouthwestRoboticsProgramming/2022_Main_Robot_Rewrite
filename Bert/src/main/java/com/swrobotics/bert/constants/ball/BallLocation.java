package com.swrobotics.bert.constants.ball;

public class BallLocation {
    private final double x;
    private final double y;
    public BallLocation(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}
